// here's an example of a for loop
for (var i = 1; i <= 5; i++) {
  console.log(i);
}

// create a loop that starts at 50 and loops until 100.
for (let q =50; q <= 100; q++) {
  console.log(q);
}
// create a loop that starts at 0 and loops to 50, incrementing by 5 each iteration.
for (let w =0; w <= 50; w+=5) {
  console.log(w);
}

// create a loop that starts at 100 and counts down to 1.
for (let z =100; z >=1; z--) {
  console.log(z);
}
